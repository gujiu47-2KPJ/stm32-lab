void OLED_WriteByte(uint8_t dat, uint8_t cmd) {
    uint8_t buffer[2];
    buffer[0] = cmd ? 0x40 : 0x00;
    buffer[1] = dat;
    HAL_I2C_Master_Transmit(&hi2c1, OLED_I2C_ADDR, buffer, 2, 100);
}

void OLED_Init(void) {
    HAL_Delay(100);
    OLED_WriteByte(0xAE, 0); //display off
    OLED_WriteByte(0x20, 0); //Set Memory Addressing Mode    
    OLED_WriteByte(0x10, 0); //00,Horizontal Addressing Mode;01,Vertical Addressing Mode;10,Page Addressing Mode (RESET);11,Invalid
    OLED_WriteByte(0xB0, 0); //Set Page Start Address for Page Addressing Mode,0-7
    OLED_WriteByte(0xC8, 0); //Set COM Output Scan Direction
    OLED_WriteByte(0x00, 0); //---set low column address
    OLED_WriteByte(0x10, 0); //---set high column address
    OLED_WriteByte(0x40, 0); //--set start line address
    OLED_WriteByte(0x81, 0); //--set contrast control register
    OLED_WriteByte(0xFF, 0); //128
    OLED_WriteByte(0xA1, 0); //--set segment re-map 0 to 127
    OLED_WriteByte(0xA6, 0); //--set normal display
    OLED_WriteByte(0xA8, 0); //--set multiplex ratio(1 to 64)
    OLED_WriteByte(0x3F, 0); //
    OLED_WriteByte(0xA4, 0); //0xa4,Output follows RAM content;0xa5,Output ignores RAM content
    OLED_WriteByte(0xD3, 0); //-set display offset
    OLED_WriteByte(0x00, 0); //-not offset
    OLED_WriteByte(0xD5, 0); //--set display clock divide ratio/oscillator frequency
    OLED_WriteByte(0xF0, 0); //--set divide ratio
    OLED_WriteByte(0xD9, 0); //--set pre-charge period
    OLED_WriteByte(0x22, 0); //
    OLED_WriteByte(0xDA, 0); //--set com pins hardware configuration
    OLED_WriteByte(0x12, 0);
    OLED_WriteByte(0xDB, 0); //--set vcomh
    OLED_WriteByte(0x20, 0); //0x20,0.77xVcc
    OLED_WriteByte(0x8D, 0); //--set DC-DC enable
    OLED_WriteByte(0x14, 0); //
    OLED_WriteByte(0xAF, 0); //--turn on oled panel
}

void OLED_Clear(void) {
    uint8_t i, n;
    for(i = 0; i < 8; i++) {
        OLED_WriteByte(0xB0 + i, 0);    //设置页地址（0~7）
        OLED_WriteByte(0x00, 0);        //设置列地址的低地址
        OLED_WriteByte(0x10, 0);        //设置列地址的高地址
        for(n = 0; n < 128; n++) {
            OLED_WriteByte(0, 1);       //写入数据
        }
    }
}

void OLED_DrawPoint(uint8_t x, uint8_t y, uint8_t color) {
    uint8_t pos, bx, temp = 0;
    if(x > 127 || y > 63) return; //超出范围了
    pos = 7 - y / 8; //找到对应的页
    bx = y % 8;      //找到对应的位
    temp = 1 << (7 - bx);
    if(color) {
        //这里需要一个缓冲区来存储屏幕内容，简化起见直接写
        OLED_Set_Pos(x, y / 8);
        OLED_WriteByte(temp, 1);
    } else {
        OLED_Set_Pos(x, y / 8);
        OLED_WriteByte(~temp, 1);
    }
}

void OLED_Set_Pos(uint8_t x, uint8_t y) {
    OLED_WriteByte(0xB0 + y, 0);    //设置页地址（0~7）
    OLED_WriteByte(((x & 0xF0) >> 4) | 0x10, 0); //设置列地址的高地址
    OLED_WriteByte((x & 0x0F), 0);  //设置列地址的低地址
}

void OLED_ShowChar(uint8_t x, uint8_t y, uint8_t chr, uint8_t size) {
    uint8_t c = chr - ' ';
    uint8_t i, j;
    uint8_t y0 = y;
    
    if(x > 126 || y > 56) return;
    
    for(i = 0; i < 6; i++) {
        for(j = 0; j < 8; j++) {
            if(F6x8[c][i] & (1 << j)) {
                OLED_DrawPoint(x + i, y0 + j, 1);
            } else {
                OLED_DrawPoint(x + i, y0 + j, 0);
            }
        }
    }
}

void OLED_ShowString(uint8_t x, uint8_t y, char *str) {
    uint8_t x0 = x;
    while((*str <= '~') && (*str >= ' ')) {
        if(x > 126) {
            x = x0;
            y += 8;
        }
        if(y > 56) return;
        OLED_ShowChar(x, y, *str, 12);
        str++;
        x += 6;
    }
}

void OLED_ShowNum(uint8_t x, uint8_t y, uint32_t num, uint8_t len) {
    uint8_t t, temp;
    uint8_t enshow = 0;
    
    for(t = 0; t < len; t++) {
        temp = (num / power(10, len - t - 1)) % 10;
        if(enshow == 0 && t < (len - 1)) {
            if(temp == 0) {
                OLED_ShowChar(x + t * 6, y, ' ', 12);
                continue;
            } else {
                enshow = 1;
            }
        }
        OLED_ShowChar(x + t * 6, y, temp + '0', 12);
    }
}

void OLED_ShowFloat(uint8_t x, uint8_t y, float num, uint8_t decimal_places) {
    char str[20];
    sprintf(str, "%.*f", decimal_places, num);
    OLED_ShowString(x, y, str);
}

